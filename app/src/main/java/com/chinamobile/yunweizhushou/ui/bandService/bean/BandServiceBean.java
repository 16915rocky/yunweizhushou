package com.chinamobile.yunweizhushou.ui.bandService.bean;

public class BandServiceBean {

	private String city;
	private String todayorder;
	private String ontheorder;
	private String proportion;
	private String handle;
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getTodayorder() {
		return todayorder;
	}
	public void setTodayorder(String todayorder) {
		this.todayorder = todayorder;
	}
	public String getOntheorder() {
		return ontheorder;
	}
	public void setOntheorder(String ontheorder) {
		this.ontheorder = ontheorder;
	}
	public String getProportion() {
		return proportion;
	}
	public void setProportion(String proportion) {
		this.proportion = proportion;
	}
	public String getHandle() {
		return handle;
	}
	public void setHandle(String handle) {
		this.handle = handle;
	}
	
}
