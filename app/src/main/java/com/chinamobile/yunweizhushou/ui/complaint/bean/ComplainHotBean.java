package com.chinamobile.yunweizhushou.ui.complaint.bean;

public class ComplainHotBean {
	private String cmNumber;
	private String cmTime;
	private String id;
	private String progress;
	private String reason;
	private String record;
	private String title;

	public String getCmNumber() {
		return cmNumber;
	}

	public String getCmTime() {
		return cmTime;
	}

	public String getId() {
		return id;
	}

	public String getProgress() {
		return progress;
	}

	public String getReason() {
		return reason;
	}

	public String getRecord() {
		return record;
	}

	public String getTitle() {
		return title;
	}

	public void setCmNumber(String cmNumber) {
		this.cmNumber = cmNumber;
	}

	public void setCmTime(String cmTime) {
		this.cmTime = cmTime;
	}

	public void setId(String id) {
		this.id = id;
	}

	public void setProgress(String progress) {
		this.progress = progress;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public void setRecord(String record) {
		this.record = record;
	}

	public void setTitle(String title) {
		this.title = title;
	}

}
