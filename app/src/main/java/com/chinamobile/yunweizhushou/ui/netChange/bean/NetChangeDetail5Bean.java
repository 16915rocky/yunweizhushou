package com.chinamobile.yunweizhushou.ui.netChange.bean;

public class NetChangeDetail5Bean {

	private String name;// 配置名称
	private String bm_config;// BM配置
	private String on_line_config;// 上线组配置
	private String issue_person;// 发布人员
	private String config_condition;// 配置状态

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBm_config() {
		return bm_config;
	}

	public void setBm_config(String bm_config) {
		this.bm_config = bm_config;
	}

	public String getOn_line_config() {
		return on_line_config;
	}

	public void setOn_line_config(String on_line_config) {
		this.on_line_config = on_line_config;
	}

	public String getIssue_person() {
		return issue_person;
	}

	public void setIssue_person(String issue_person) {
		this.issue_person = issue_person;
	}

	public String getConfig_condition() {
		return config_condition;
	}

	public void setConfig_condition(String config_condition) {
		this.config_condition = config_condition;
	}

}
