package com.chinamobile.yunweizhushou.ui.complaint.bean;

public class ReportFormBean {
	private String inRate;
	private String newRate;
	private String name;
	private String num;

	public String getInRate() {
		return inRate;
	}

	public String getNewRate() {
		return newRate;
	}

	public String getName() {
		return name;
	}

	public String getNum() {
		return num;
	}

	public void setInRate(String inRate) {
		this.inRate = inRate;
	}

	public void setNewRate(String newRate) {
		this.newRate = newRate;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setNum(String num) {
		this.num = num;
	}

}
