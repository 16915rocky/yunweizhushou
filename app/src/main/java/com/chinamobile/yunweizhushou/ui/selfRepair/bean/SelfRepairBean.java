package com.chinamobile.yunweizhushou.ui.selfRepair.bean;

public class SelfRepairBean {

	private String fault_reason;
	private String fault_times;
	private String hostip;
	private String server_name;
	private String time;
	public String getFault_reason() {
		return fault_reason;
	}
	public void setFault_reason(String fault_reason) {
		this.fault_reason = fault_reason;
	}
	public String getFault_times() {
		return fault_times;
	}
	public void setFault_times(String fault_times) {
		this.fault_times = fault_times;
	}
	public String getHostip() {
		return hostip;
	}
	public void setHostip(String hostip) {
		this.hostip = hostip;
	}
	public String getServer_name() {
		return server_name;
	}
	public void setServer_name(String server_name) {
		this.server_name = server_name;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
