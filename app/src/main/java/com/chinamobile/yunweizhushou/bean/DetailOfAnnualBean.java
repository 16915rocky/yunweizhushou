	public void setBusiness_out_score(String business_out_score) {
		this.business_out_score = business_out_score;
	}
	public String getConsistency_score() {
		return consistency_score;
	}
	public void setConsistency_score(String consistency_score) {
		this.consistency_score = consistency_score;
	}
	public String getAccuracy_score() {
		return accuracy_score;
	}
	public void setAccuracy_score(String accuracy_score) {
		this.accuracy_score = accuracy_score;
	}
